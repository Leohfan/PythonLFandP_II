import turtle

# 直接调用turtle函数控制默认海龟
turtle.setup(350,300) # 设置屏幕的宽度和高度
turtle.forward(100)   # 向前移动100像素
turtle.right(90)      # 右转90度
turtle.forward(100)   # 向前移动100像素
turtle.right(90)      # 右转90度
turtle.forward(100)   # 向前移动100像素
turtle.right(90)      # 右转90度
turtle.forward(100)   # 向前移动100像素

turtle.done()         # 结束绘图，保持窗口打开